stringa = 'Python'

print(stringa)

print('****************************************')

print(stringa[0])  # elemento in posizione 0 (il primo)

print('****************************************')

print(stringa[5])  # elemento in posizione 5 (il sesto)

print('****************************************')

print(stringa[-1])  # elemento in posizione -1 (l'ultimo)

print('****************************************')

print(stringa[-4])  # elemento in posizione -4 (il quartultimo)

#slicing

s = 'Python'

print(s[0:2])  # sottostringa con elementi da 0 (incluso) a 2 (escluso)

print(s[:2])  # dall'inizio all'elemento con indice 2 (escluso)

print(s[3:5])  # dall'elemento con indice 3 (incluso) a 5 (escluso)

print(s[4:])   # dall'elemento con indice 4 (incluso) alla fine

print(s[-2:])  # dall'elemento con indice -2 (incluso) alla fin